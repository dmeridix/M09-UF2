
public class Forquilla {
    
    private int numero; 
    private boolean enUs;
    
    public Forquilla(int numero){
        this.numero = numero;
        enUs = false;
    }
    
    public int getNumero() {
        return numero;
    }
    public void setNumero(int numero) {
        this.numero = numero;
    }
    public boolean isEnUs() {
        return enUs;
    }
    public void setEnUs(boolean enUs) {
        this.enUs = enUs;
    }


}
